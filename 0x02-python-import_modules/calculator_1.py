#!/usr/bin/python3
def add(a, b):
    """addition function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """subtraction function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """ multiplication function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """division function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        The return value. a / b
    """
    return int(a / b)
